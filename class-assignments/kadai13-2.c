/*
キーボードからの文字列入力と、文字列を扱うライブラリ関数を使用して、
ユーザーが寿司の注文をする処理をシミュレーションするプログラムを作成する。
*/
#include <stdio.h>
#include <string.h>

int main(void) {
    // 寿司ネタのリスト（メニュー）
    char menu[10][20] = {
        "まぐろ", "サーモン", "えび", "いくら", "たまご",
        "はまち", "いか", "ほたて", "あなご", "かんぱち"
    };

    // ユーザーからの入力を受け取るための変数
    char input[100];
    // 注文されたネタを連結していくための変数
    char order_list[500];
    // ループ用のカウンタ
    int i;
    // ネタが見つかったかどうかを判定するためのフラグ
    int found_flag;

    // 注文リストを空の状態で初期化
    order_list[0] = '\0';

    printf("寿司の注文どうぞ\n");

    // "わさび"が入力されるまで注文を繰り返す
    while (1) {
        scanf("%s", input);

        // 入力が"わさび"ならループを抜ける
        if (strcmp(input, "わさび") == 0) {
            break;
        }

        // フラグを初期化（0は見つかっていない状態）
        found_flag = 0;

        // 入力されたネタがメニューにあるか探す
        for (i = 0; i < 10; i++) {
            // strcmpで文字列を比較。一致すれば0が返る
            if (strcmp(input, menu[i]) == 0) {
                // ネタが見つかったので注文リストに追加
                strcat(order_list, input);
                strcat(order_list, " "); // ネタの間にスペースを入れる
                found_flag = 1; // 見つかったのでフラグを1にする
                break; // メニューが見つかったので、これ以上探す必要はない
            }
        }

        // forループを抜けた後、フラグをチェック
        if (found_flag == 1) {
            // ネタが見つかった場合
            printf("他はいかがですか？\n");
        } else {
            // ネタが見つからなかった場合
            printf("申し訳ありません。このネタはありません。\n");
            printf("他はいかがですか？\n");
        }
    }

    // 最終的な注文内容を表示
    printf("注文ありがとうございました：%s\n", order_list);

    return 0;
}