/*
条件に合わせてビットのシフトかビット反転を行うプログラム
*/

#include <stdio.h>

int main(void)
{
    int x = 13;  // 変数xを13で初期化

    while (x != 0) {  // xが0でない間繰り返す
        printf("現在のxの値: %d\n", x);  // 現在のxの値を表示

        if (x % 2 == 0) {
            // 偶数の場合、右に1ビットシフト
            x = x >> 1;
            printf("偶数なので右シフト → %d\n", x);
        } 
        
        if (x % 2 == 1 || x % 2 == -1) {
            // 奇数の場合、ビット反転
            x = ~x;
            printf("奇数なのでビット反転 → %d\n", x);
        }
    }
    
    // 最後にxが０になったことを表示
    printf("最終的なxの値は0です\n");
    return 0;
}

